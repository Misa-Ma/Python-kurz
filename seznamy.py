#SEZNAM S VNOŘENÝMI SLOVNÍKY - každý prvek je slovník
seznam = [
    {"klíč1": "hodnota1", "klíč2": "hodnota2"},
    {"klíč3": "hodnota3", "klíč4": "hodnota4"}
]

#SLOVNÍK S VNOŘENÝMI SEZNAMY - jedná se o slovník, kde každá hodnota je seznam
slovník = {
    "klíč1": ["hodnota1", "hodnota2"],
    "klíč2": ["hodnota3", "hodnota4"]
}

#DVOUROZMĚRNÝ SEZNAM - Jedná se o seznam, kde každý prvek je další seznam, což vytváří matici. Například:
dvourozměrný_seznam = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]

#DVOUROZMĚRNÝ SLOVNÍK - Tento termín se běžně nepoužívá, ale může se jednat o slovník, kde každá hodnota je další slovník. Například:
dvourozměrný_slovník = {
    "klíč1": {"podklíč1": "hodnota1", "podklíč2": "hodnota2"},
    "klíč2": {"podklíč3": "hodnota3", "podklíč4": "hodnota4"}
}